package com.example.project.Data;

public class ChatData {
    public String name;
    public String message;
    public String profileUrl;
    public String time;

    public ChatData() {}

    public ChatData(String name, String message, String profileUrl, String time) {
        this.name = name;
        this.message = message;
        this.profileUrl = profileUrl;
        this.time = time;
    }

}
